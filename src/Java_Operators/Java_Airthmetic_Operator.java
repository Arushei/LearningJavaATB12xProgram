package Java_Operators;

public class Java_Airthmetic_Operator {
    public static void main(String[] args) {

        // a+b = Addition
        //a-b = Subraction
        //a*b = Multiple
        //a/b = Division
        //a%b = Modulus (Remainder)

        int a = 20;
        int b = 3;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c);
        System.out.println(a*c);
        System.out.println("a+b"); //"" output print as it is

    }
}
