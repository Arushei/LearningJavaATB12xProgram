package Java_Operators;

public class Java_Logical_operator {
    public static void main(String[] args) {

        // && AND
        // || OR
        // ! NOT

        boolean a = true;
        System.out.println(!a);

        boolean b = true;
        System.out.println(!!b); //Even Not's cancel each other.

        boolean c = true || false; //Electronics AND, OR Gate concept.
        System.out.println(c);

        boolean c1 = false && true;
        System.out.println(c1);





    }
}
