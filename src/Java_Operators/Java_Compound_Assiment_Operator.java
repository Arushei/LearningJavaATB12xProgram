package Java_Operators;

public class Java_Compound_Assiment_Operator {
    public static void main(String[] args) {

        int age = 10;
        age += 10; // it means age = age + 10;
        System.out.println(age);

        int age1 = 20;
        age1 -= 10; // it means age = age - 10;
        System.out.println(age1);

        int age2 = 30;
        age2 /= 10; // it means age = age / 10;
        System.out.println(age2);
    }
}
