package Java_Operators;

public class Java_Interview_Operator {
    public static void main(String[] args) {

        //Char is also int.
        // So in below case their ASCII value will be used.
        char A1 = 'A'; //ASCII -> 65
        char B1 = 'B'; //ASCII -> 66
        System.out.println(A1+B1); // it will print 131.

        short s = 10;
        char c = 'A';
        System.out.println(s+c); //output -> 75


    }
}
