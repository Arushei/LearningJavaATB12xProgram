package Java_Increment_Decrement_OP;

public class Java_Increment_OP {
    public static void main(String[] args) {

        //Increment Operator (++)
        //**Prefix Increment**: `++variable`
        // value is incremented first and then stored in the result.

        // ++a = a+1, increment by 1

        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        //ERT Table (Expression & Result Table)
        // Line No. | a value | b value
        /*    12    |   10    | NA
              13    | 11      |  11
              14    | 11-print| 11
              15    | 11      | 11 - print.
         */


    }
}
