package Java_Increment_Decrement_OP;

public class Java_Decrement {
    public static void main(String[] args) {
      //  int a = 10;
     //   int result_post = a--; // Print first decrement later.
     //   System.out.println(result_post);
     //   System.out.println(a);

        // a-- -> 10
        //a -> 9

        int a = 10;
        int result_pre = --a; // Decrement first print later.
        System.out.println(result_pre);
        System.out.println(a);

        // --a -> 9
        // a -> 9
    }
}
